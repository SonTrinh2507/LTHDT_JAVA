    private void tbl_nvMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_nvMousePressed
        txtMaNV.setEnabled(false);
        btnThem.setEnabled(false);
        btnXemTK.setEnabled(true);
        baoloi.setText("");// TODO add your handling code here:
        int row = tbl_nv.getSelectedRow();
        txtMaNV.setText((String) tbl_nv.getValueAt(row, 1));
        txtHoNV.setText((String) tbl_nv.getValueAt(row, 2));
        TxtTen.setText((String) tbl_nv.getValueAt(row, 3));
        cbbGT.setSelectedItem((String) tbl_nv.getValueAt(row, 4));
        String dateNS = tbl_nv.getValueAt(tbl_nv.getSelectedRow(), 5).toString();
        txtDC.setText((String) tbl_nv.getValueAt(row, 6));
        txtSDT.setText((String) tbl_nv.getValueAt(row, 7));
        // cbbTrangThai.setSelectedItem((String) tbl_nv.getValueAt(row, 8));

        Object value = tbl_nv.getValueAt(row, 8);
        if (value != null) {
            int selectedValue = Integer.parseInt(value.toString());
            for (Map.Entry<String, Integer> entry : comboBoxItems.entrySet()) {
                if (entry.getValue() == selectedValue) {
                    cbbTrangThai.setSelectedItem(entry.getKey());
                    break;
                }
            }
        }

        dateNS = dateNS.substring(8, 10) + "/" + dateNS.substring(5, 7) + "/" + dateNS.substring(0, 4);

        try {
            java.util.Date datetmp = new SimpleDateFormat("dd/MM/yyyy").parse(dateNS);
            txtNS.setDate(datetmp);
        } catch (ParseException ex) {
            Logger.getLogger(GiaoDienNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        selectedImageUrl = tbl_nv.getValueAt(row, 9).toString();
        ImageIcon imageIcon = new ImageIcon(tbl_nv.getValueAt(row, 9).toString());
        Image image = imageIcon.getImage().getScaledInstance(lableAnh.getWidth(), lableAnh.getHeight(), Image.SCALE_SMOOTH);
        lableAnh.setIcon(new ImageIcon(image));

        // TODO add your handling code here:
    }//GEN-LAST:event_tbl_nvMousePressed

