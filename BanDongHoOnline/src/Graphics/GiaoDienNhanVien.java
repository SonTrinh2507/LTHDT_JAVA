    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        // TODO add your handling code here:
        if (txtSDT.getText().length() != 10) {
            baoloi.setText("thông tin về số điện thoại không hợp lệ!");
            return;
        } else {
            for (int i = 0; i < txtSDT.getText().length(); i++) {
                if (Character.isDigit(txtSDT.getText().charAt(i))) {
                    continue;
                } else {
                    baoloi.setText("thông tin về số điện thoại không hợp lệ!");
                    return;
                }

            }
        }

        if (TxtTen.getText().equals("") || txtDC.getText().equals("") || txtHoNV.getText().equals("") || txtNS.getDate().equals("") || txtSDT.getText().equals("") || cbbTrangThai.getSelectedItem().equals(null)) {
            baoloi.setText("Vui lòng nhập đầy đủ thông tin nhân viên!");
        } else {
            int row = tbl_nv.getSelectedRow();
            String GT = (String) cbbGT.getSelectedItem();
            String selectedItem = (String) cbbTrangThai.getSelectedItem();
            int TT = comboBoxItems.get(selectedItem);
            String HinhAnh = selectedImageUrl;
            NhanVien nv = new NhanVien(txtMaNV.getText(), txtHoNV.getText(), TxtTen.getText(), GT, txtDC.getText(), txtSDT.getText(), TT, txtNS.getDate(), HinhAnh);
            ChucVu cv = null;

            if (tbl_nv.getSelectedRow() == -1) {

                for (NhanVien nvv : Controller.controller.arrayListNhanVien) {
                    if (nvv.getMaNV().equals(txtMaNV.getText())) {
                        baoloi.setText("Nhân viên đã tồn tại!");
                        return;
                    }
                }
                for (TaiKhoan tkk : Controller.controller.arrayListTaiKhoan) {
                    if (txtMaNV.getText().equals(tkk.getMaNV())) {
                        baoloi.setText("Tên đăng nhập đã tồn tại!");
                        return;
                    }
                }

                CONNECTION.InsertData.insertNhanVien(nv);
                showData();

                tbl_nv.getSelectionModel().setSelectionInterval(tbl_nv.getRowCount() - 1, tbl_nv.getRowCount() - 1);

            } else {

                CONNECTION.UpdateData.updateNhanVien(nv);
                TaiKhoan tk = new TaiKhoan(nv.getMaNV(), txtSDT.getText(), 0, cv, "1");

                CONNECTION.UpdateData.upadateTaiKhoan(tk);

            }
            showData();
            tbl_nv.getSelectionModel().setSelectionInterval(row, row);

        }
        String x = "";
        TxtTen.setText(x);
        txtDC.setText(x);
        cbbGT.setSelectedItem(null);
        txtHoNV.setText(x);
        txtMaNV.setText(x);
        txtNS.setDate(null);
        txtSDT.setText(x);
        btnThem.setEnabled(true);
        cbbTrangThai.setSelectedItem(null);
        lableAnh.setIcon(null);

        showData();
// TODO add your handling code here:
    }//GEN-LAST:event_btnThemActionPerformed

