public void showTheoNgayBD(String d1, String d2){
     DataConnection.createStatement();
         try {
            String sql = ("select * from dbo.KhuyenMai where NgayBD between ? and ?");
            PreparedStatement ps = DataConnection.connection.prepareStatement(sql);
            ps.setString(1,d1);
            ps.setString(2, d2);
            ResultSet rs = ps.executeQuery();
            DefaultTableModel mode = (DefaultTableModel) jTable1.getModel();
            Object[] row;
            int dem = 1;
            while(rs.next()){
            row = new Object[6];
            row[0] = dem++;
            row[1] = rs.getString("MaKM");
            row[2] = rs.getString("MaNV");
            row[3] = rs.getDate("LyDo");
            row[4] = rs.getDate("NgayBD");
            row[5] = rs.getDate("NgayKT");
            mode.addRow(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(GiaoDienKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
