    private void showCTKM() {
        myTable2 = (DefaultTableModel) jTable2.getModel();
        new LoadData();
        int x = jTable1.getSelectedRow();
        int dem2 = 0;
        myTable2.setRowCount(0);
        String n = (String) myTable.getValueAt(x, 1);
        for (CT_KhuyenMai ctkm : Controller.controller.arrayListCT_KhuyenMai) {
            dem2++;
            if (ctkm.getMaKM().equals(n)) {
                myTable1.addRow(new Object[]{dem2, ctkm.getMaKM(), ctkm.getMaDH(), ctkm.getPTKM()});
            }
        }
    }
